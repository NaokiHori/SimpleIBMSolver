def get_index_2d(uname, xs, ys):
    if xs == "0":
        ipart = "(I  )"
    elif int(xs) > 0:
        ipart = "(I-{:d})".format(+int(xs))
    elif int(xs) < 0:
        ipart = "(I+{:d})".format(-int(xs))
    else:
        print("xs:", xs)
        print("To Be Implemented")
        sys.exit()
    if ys == "0":
        jpart = "(J  )"
    elif int(ys) > 0:
        jpart = "(J-{:d})".format(+int(ys))
    elif int(ys) < 0:
        jpart = "(J+{:d})".format(-int(ys))
    else:
        print("ys:", ys)
        print("To Be Implemented")
        sys.exit()
    return f"{jpart} * {uname}_SIZE_0 + {ipart}"

