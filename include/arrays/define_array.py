def get_size(s, e):
    if debug:
        print("get_size, s and e:", s, e)
    # extract numbers
    s_ = re.sub("[a-zA-Z]", "", s)
    e_ = re.sub("[a-zA-Z]", "", e)
    # extract prefix
    string = re.sub("[0-9+-]", "", e)
    s_ = int(s_)
    if e_ == "":
        e_ = 0
    else:
        e_ = int(e_)
    size = "({}{:+d})".format(string, e_ - s_ + 1)
    if debug:
        print("get_size, size:", size)
    return size

def generate_grid_nd(f, name, xs, xe, ys, ye, zs, ze):
    name = name.strip()
    uname = name.upper()
    index_2d = get_index_2d(uname, xs, ys)
    index_3d = get_index_3d(uname, xs, ys, zs)
    size0 = get_size(xs, xe)
    size1 = get_size(ys, ye)
    size2 = get_size(zs, ze)
    format_dict = {
            "name": name,
            "uname": uname,
            "index_2d": index_2d,
            "index_3d": index_3d,
            "size0": size0,
            "size1": size1,
            "size2": size2,
    }
    string = list()
    # header
    string.append((
            "/*** {name} ***/\n"
    ))
    # 2D array (not for z-related things)
    if not "z" in name:
        string.append((
            "#if NDIMS == 2\n"
            "#define {uname}_SIZE_0 {size0}\n"
            "#define {uname}_SIZE_1 {size1}\n"
            "#define {uname}(I, J) ({name}[ {index_2d} ])\n"
            "#endif // NDIMS == 2\n"
        ))
    # 3D array
    string.append((
            "#if NDIMS == 3\n"
            "#define {uname}_SIZE_0 {size0}\n"
            "#define {uname}_SIZE_1 {size1}\n"
            "#define {uname}_SIZE_2 {size2}\n"
            "#define {uname}(I, J, K) ({name}[ {index_3d} ])\n"
            "#endif // NDIMS == 3\n"
    ))
    # footer
    string.append((
            "/*** {name} ***/\n\n"
    ))
    string = "".join(string)
    string = string.format(**format_dict)
    f.write(string)

